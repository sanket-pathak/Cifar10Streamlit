def predict_custom_image(model, image_path, classes):
    # Load image from file
    img = tf.keras.utils.load_img(image_path, target_size=(32, 32))
    img_array = tf.keras.utils.img_to_array(img)  # (32,32,3)

    # Normalize (same as training)
    img_array = img_array / 255.0

    # Add batch dimension: (1,32,32,3)
    img_input = np.expand_dims(img_array, axis=0)

    # Predict
    pred_probs = model.predict(img_input)
    pred_class = np.argmax(pred_probs, axis=1)[0]

    # Show image + prediction
    plt.imshow(img)
    plt.axis("off")
    plt.title(f"Predicted: {classes[pred_class]}")
    plt.show()

    print("Predicted class:", classes[pred_class])

# Example usage:
