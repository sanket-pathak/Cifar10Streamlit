def predict_test_image(model, X_test, y_test, classes, index):
    img = X_test[index]                      # shape (32,32,3)
    img_input = np.expand_dims(img, axis=0)  # shape (1,32,32,3)

    pred_probs = model.predict(img_input)
    pred_class = np.argmax(pred_probs, axis=1)[0]

    actual_class = y_test[index]

    plt.imshow(img)
    plt.axis("off")
    plt.title(f"Predicted: {classes[pred_class]} | Actual: {classes[actual_class]}")
    plt.show()

    if pred_class == actual_class:
        print("✅ Correct Prediction")
    else:
        print("❌ Wrong Prediction")

# Example usage:
